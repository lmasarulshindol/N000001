/**
 * キャラ・場所・好感度から context line セットを取得。
 * フォールバック: spot未定義→default、tier未定義→early、key未定義→null
 *
 * @param {string} characterId
 * @param {string} spotId
 * @param {number} affection 0〜100
 * @returns {{foreplayIntro:string[]|null, insertReady:string|null, climaxLine:string|null, aftercareLine:string|null}}
 */
function getContextualLines(characterId, spotId, affection) {
    const tier = resolveAffectionTier(affection);
    const charTable = CONTEXTUAL_INTIMATE_LINES[characterId] || {};
    const spotTable = charTable[spotId] || charTable.default || {};
    const tierLines = spotTable[tier] || spotTable.early || {};
    return {
        foreplayIntro: tierLines.foreplayIntro || null,
        insertReady: tierLines.insertReady || null,
        climaxLine: tierLines.climaxLine || null,
        aftercareLine: tierLines.aftercareLine || null
    };
}

/**
 * 指定キャラが指定 spot のセリフを持っているかを判定。
 * @returns {boolean}
 */
function hasContextualSpot(characterId, spotId) {
    return !!(CONTEXTUAL_INTIMATE_LINES[characterId]?.[spotId]);
}

